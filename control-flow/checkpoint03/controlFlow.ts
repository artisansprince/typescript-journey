// 3. Control Flow

// If-Else
let number = 7;
if (number > 10) {
  console.log("Number is greater than 10.");
} else {
  console.log("Number is 10 or less.");
}

// Switch Case
let day = 3;
switch (day) {
  case 1:
    console.log("Monday");
    break;
  case 2:
    console.log("Tuesday");
    break;
  case 3:
    console.log("Wednesday");
    break;
  default:
    console.log("Another day");
}

// For Loop
for (let i = 0; i < 5; i++) {
  console.log(`Iteration ${i}`);
}

// While Loop
let counter = 0;
while (counter < 3) {
  console.log(`Counter: ${counter}`);
  counter++;
}
